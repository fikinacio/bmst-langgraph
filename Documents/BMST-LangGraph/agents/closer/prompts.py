# agents/closer/prompts.py — LLM prompts and Pydantic schemas for the CLOSER agent

from __future__ import annotations

from typing import Literal

from pydantic import BaseModel, Field


# ── Pydantic schemas for structured LLM output ───────────────────────────────

class DiagnosisExtractSchema(BaseModel):
    """Extracted structured information from a prospect's diagnostic answer."""
    problema_principal: str = Field(
        ..., description="One-sentence description of the main pain point identified."
    )
    volume_estimado_dia: int | None = Field(
        None, description="Estimated messages or calls per day (if mentioned)."
    )
    tem_equipa_dedicada: bool | None = Field(
        None, description="True if the prospect has a dedicated team for client comms."
    )
    diagnostico_completo: bool = Field(
        ..., description="True only if all 3 diagnostic questions have been answered."
    )


class SolucaoSchema(BaseModel):
    """Solution selection and price range based on the full diagnosis."""
    servico_recomendado: str = Field(
        ...,
        description=(
            "Exact name of the recommended BMST service. "
            "One of: 'Chatbot WhatsApp básico', 'Chatbot WA com IA + RAG', "
            "'Website WordPress', 'Automação de processos', "
            "'Análise de dados com IA', 'Proposta integrada'."
        ),
    )
    justificacao: str = Field(..., description="One-sentence justification.")
    valor_minimo_aoa: int = Field(..., ge=180_000, description="Minimum price in AOA.")
    valor_maximo_aoa: int = Field(..., ge=180_000, description="Maximum price in AOA.")
    prazo_semanas: int = Field(..., ge=1, le=26, description="Estimated delivery in weeks.")
    beneficio_1: str = Field(..., description="Primary concrete benefit for the prospect.")
    beneficio_2: str = Field(..., description="Secondary concrete benefit.")


class PropostaDraftSchema(BaseModel):
    """Full commercial proposal draft generated by the CLOSER."""
    cliente: str
    decisor: str
    problema_identificado: str
    solucao_proposta: str
    entregaveis: list[str] = Field(..., min_length=2, max_length=6)
    prazo_semanas: int = Field(..., ge=1, le=26)
    valor_aoa: int = Field(..., ge=180_000)
    condicoes_pagamento: str = Field(
        default="50% assinatura + 50% antes entrega"
    )
    validade_proposta_dias: int = Field(default=15)
    notas_fundador: str = Field(
        default="", description="Context for the founder's approval review."
    )


class RespostaPropostaSchema(BaseModel):
    """Classification of the prospect's response to the sent proposal."""
    classificacao: Literal[
        "ACEITE",
        "OBJECAO_PRECO",
        "OBJECAO_PRAZO",
        "PRECISA_PENSAR",
        "RECUSA",
    ]
    objecao_principal: str | None = Field(
        None, description="Core objection text if classificacao is an OBJECAO_*."
    )


class ObjecaoResponseSchema(BaseModel):
    """Objection-handling response text generated by the CLOSER."""
    resposta: str = Field(
        ..., description="Natural, consultant-tone response to the objection."
    )
    estrategia: Literal[
        "reduzir_ambito",
        "esclarecer_valor",
        "dar_tempo",
        "segunda_opiniao",
        "encerrar_educadamente",
    ]


