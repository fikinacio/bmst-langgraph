# agents/ledger/prompts.py — LEDGER agent prompts and output schemas
#
# All LLM prompts instruct the model to write in Portuguese (Angola).
# The system prompt defines rules and persona in English for maintainability.

from __future__ import annotations

from typing import Literal

from pydantic import BaseModel


# ── Output schemas ────────────────────────────────────────────────────────────

class FacturaNotificacaoSchema(BaseModel):
    """WhatsApp message notifying the client that a new invoice has been issued."""
    mensagem: str   # full message text in Portuguese (Angola)


class LembreteSchema(BaseModel):
    """
    Payment reminder message generated by the LLM.

    urgencia reflects the escalating tone: amigavel (D+3) → profissional (D+7)
    → firme (D+14).  Only D+14 reminders require founder approval before sending.
    """
    mensagem: str
    urgencia: Literal["amigavel", "profissional", "firme"]
    requer_aprovacao_fundador: bool


class RelatorioSchema(BaseModel):
    """Monthly financial report summary for the BMST founder."""
    mes_ano: str            # e.g. "Abril 2026"
    total_faturado: int     # AOA — all invoices issued this month
    total_recebido: int     # AOA — invoices confirmed as paid
    total_em_atraso: int    # AOA — invoices past due date and unpaid
    num_facturas: int
    num_pagas: int
    num_atraso: int
    destaques: list[str]    # 2–4 actionable insights for the founder


